function generateCSV() {
  const headers = [
    "Test Case ID",
    "Page/Module",
    "Feature/Functionality",
    "Test Description",
    "Pre-conditions",
    "Input Parameters",
    "Example Value",
    "Expected Output",
    "Status (Pass/Fail)",
    "Remarks"
  ];

  const escapeCSV = (str) => {
    if (str === null || str === undefined) return '""';
    const clean = str.replace(/"/g, '""');
    return `"${clean}"`;
  };

  let csvContent = headers.map(escapeCSV).join(',') + '\n';

  testCases.forEach((tc) => {
    const row = [
      tc.id,
      tc.module,
      tc.functionality,
      tc.desc,
      tc.pre,
      tc.params,
      tc.example,
      tc.expected,
      tc.status,
      tc.remarks
    ];
    csvContent += row.map(escapeCSV).join(',') + '\n';
  });

  const outputPath = path.join(__dirname, 'test_cases.csv');
  fs.writeFileSync(outputPath, csvContent, 'utf8');
  console.log(`Successfully generated ${testCases.length} test cases at ${outputPath}`);
}

generateCSV();
